package com.kh.java.basic;

public class Exam_Variable {
	public static void main(String [] args) {
		//변수의 선언
		int num;
		//변수의 초기화
		num = 1118;
		// int num = 1118; 한꺼번에(선언+초기화)
		String str = "Hello World";
		//문자를 쓰고 싶다면
		System.out.println(1118);
		System.out.println(num); //몇줄 지나고도 쓸수있다
		System.out.println(str);
		num = 515; //변수는 바뀔 수 있다
		System.out.println(num); //바뀌고 출력할 수 있다
	}

}
